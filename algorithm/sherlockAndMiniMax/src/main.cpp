#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

int minmax(int p, int q, vector<int>& array) {
	int result = -1;
	vector<int> points;
	points.push_back(p);
	int size = array.size();
	for (int i = 0; i < (size - 1); i++) {
		int mid = array[i] + array[i + 1];
		if ((mid % 2) == 0) {
			mid = mid / 2;
			if ((mid > p) && (mid < q))
				points.push_back(mid); 
		} else {
			mid = mid / 2;
			if ((mid > p) && (mid < q))
				points.push_back(mid); 
			mid = mid + 1;
			if ((mid > p) && (mid < q))
				points.push_back(mid); 
		}
	}
	points.push_back(q);

	int max = 0;
	for (int p : points) {
		int min = 1000000000;
		for (auto a : array) {
			int d = abs(p-a);
			if (min > d) {
				min = d;
			}
		}
		if (max < min) {
			max = min;
			result = p;
		}
	}
	return result;
}

int main() {
	int n = 0;
	cin >> n;
	vector<int> array;
	for (int i = 0; i < n; i++) {
		int t = 0;
		cin >> t;
		array.push_back(t);
	}
	int p = 0;
	int q = 0;
	cin >> p;
	cin >> q;

	sort(array.begin(), array.end());
	cout << minmax(p, q, array) << endl;

	return 0;
}

